    private void botonProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonProductoActionPerformed
        String nombre = "";
        int costo = 0;
        int origen = 0;
        // Verifica que todos los datos se ingresen correctamente 
        try{
            nombre = textNombre.getText();
            costo = Integer. parseInt(textCosto.getText());
            origen = Integer. parseInt((String) ComboOrigen.getSelectedItem());
            if(!"".equals(nombre) && costo != 0 && (origen == 1 || origen == 2)){
                Producto nuevoProducto = new Producto (nombre,costo,origen);
                // Confirma que el producto no esta repetido antes de agregarlo a la lista
                if(!modelo.buscarRepetidosProducto(nuevoProducto)){
                    modelo.agregarProducto(nuevoProducto);
                    listaProd.setListData(modelo.getListaNombresProductos());
                }else{
                    JOptionPane.showMessageDialog(null, "Producto repetido!",
                    "Producto repetido!", JOptionPane.WARNING_MESSAGE);
                }    
            }else if("".equals(nombre)){
                JOptionPane.showMessageDialog(null, "Datos incorrectos!",
                "Datos incorrectos!", JOptionPane.WARNING_MESSAGE);
            }   
        }catch(NumberFormatException ex ){ 
            JOptionPane.showMessageDialog(null, "Datos incorrectos!",
            "Datos incorrectos!", JOptionPane.WARNING_MESSAGE);
        }
        textNombre.setText("");
        textCosto.setText("");
        ComboOrigen.setSelectedIndex(-1);
    }//GEN-LAST:event_botonProductoActionPerformed

