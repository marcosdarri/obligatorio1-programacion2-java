    private void botonFiltrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonFiltrarActionPerformed
        //Dependiendo de que hayas seleccionado el los comobos el filtro que hace y luego actualiza lo
        // que muestra en la matriz de la ventana
        if( comboDia.getSelectedIndex() != 0 && comboProd.getSelectedIndex() == 0 ){
            miEmpresa.resetMatrizFiltrada();
            System.out.println(comboDia.getSelectedIndex());
            miEmpresa.filtrarMatrizDia(comboDia.getSelectedIndex());
            this.modelo = new modeloTabla(miEmpresa.getMatrizFumigacionFiltrada());
            jScrollPane1.setViewportView(tablaDeFumigacion);
            tablaDeFumigacion.setModel(modelo);
        }
        if( comboDia.getSelectedIndex() == 0 && comboProd.getSelectedIndex() != 0 ){
            miEmpresa.resetMatrizFiltrada();
            miEmpresa.filtrarMatrizProducto((String) comboProd.getSelectedItem());
            this.modelo = new modeloTabla(miEmpresa.getMatrizFumigacionFiltrada());
            jScrollPane1.setViewportView(tablaDeFumigacion);
            tablaDeFumigacion.setModel(modelo);
        }
        if( comboDia.getSelectedIndex() != 0 && comboProd.getSelectedIndex() != 0 ){
            miEmpresa.resetMatrizFiltrada();
            miEmpresa.filtrarMatrizProdYDia(comboDia.getSelectedIndex(),(String) comboProd.getSelectedItem());
            this.modelo = new modeloTabla(miEmpresa.getMatrizFumigacionFiltrada());
            jScrollPane1.setViewportView(tablaDeFumigacion);
            tablaDeFumigacion.setModel(modelo);
        }
        if( comboDia.getSelectedIndex() == 0 && comboProd.getSelectedIndex() == 0){
            miEmpresa.resetMatrizFiltrada();
            this.modelo = new modeloTabla(this.matrizFumigacion);
            jScrollPane1.setViewportView(tablaDeFumigacion);
            tablaDeFumigacion.setModel(modelo);
        }
        // Centra las columnas
        DefaultTableCellRenderer tcr = new DefaultTableCellRenderer();
        tcr.setHorizontalAlignment(SwingConstants.CENTER);
        for( int i = 0; i < 50; i++){
            tablaDeFumigacion.getColumnModel().getColumn(i).setCellRenderer(tcr);
        }
    }//GEN-LAST:event_botonFiltrarActionPerformed

