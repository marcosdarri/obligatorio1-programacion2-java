    // Cargamos los combos con los dias y los productos previamente cargados
    private void cargarCombos() {
        comboDia.addItem("TODOS");
        comboProd.addItem("TODOS");
        for(int i = 1; i <= 31; i++) {
            comboDia.addItem(Integer.toString(i));
        }
        comboDia.setSelectedIndex(0);
        for( int i = 0; i < miEmpresa.getListaProductos().size(); i++) {
            comboProd.addItem(miEmpresa.getListaProductos().get(i).getNombre());
        }
        comboProd.setSelectedIndex(0);
    }

